package weekone.taskone.entity;

public class NumbersData {
    private int largestNumber;
    private int smallestNumber;
    private int largestOddNumber;

    public NumbersData() {
    }

    public int getLargestNumber() {
        return largestNumber;
    }

    public void setLargestNumber(int largestNumber) {
        this.largestNumber = largestNumber;
    }

    public int getSmallestNumber() {
        return smallestNumber;
    }

    public void setSmallestNumber(int smallestNumber) {
        this.smallestNumber = smallestNumber;
    }

    public int getLargestOddNumber() {
        return largestOddNumber;
    }

    public void setLargestOddNumber(int largestOddNumber) {
        this.largestOddNumber = largestOddNumber;
    }
}
